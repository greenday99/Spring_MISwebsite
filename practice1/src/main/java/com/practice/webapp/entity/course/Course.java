package com.practice.webapp.entity.course;

public class Course {
	private String dc_code;
	private String class_sche;
	private String div_code;
	private String dc_class;
	
	public String getDc_code() {
		return dc_code;
	}
	
	public void setDc_code(String dc_code) {
		this.dc_code = dc_code;
	}
	public String getClass_sche() {
		return class_sche;
	}
	public void setClass_sche(String class_sche) {
		this.class_sche = class_sche;
	}
	public String getDiv_code() {
		return div_code;
	}
	public void setDiv_code(String div_code) {
		this.div_code = div_code;
	}
	public String getDc_class() {
		return dc_class;
	}
	public void setDc_class(String dc_class) {
		this.dc_class = dc_class;
	}
	
	

}
