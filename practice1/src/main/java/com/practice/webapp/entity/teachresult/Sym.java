package com.practice.webapp.entity.teachresult;

import java.util.Date;

public class Sym {
	private int sym_code;
	private String sym_name;
	private Date sym_time;
	private String sym_url;
	
	public int getSym_code() {
		return sym_code;
	}
	public void setSym_code(int sym_code) {
		this.sym_code = sym_code;
	}
	public String getSym_name() {
		return sym_name;
	}
	public void setSym_name(String sym_name) {
		this.sym_name = sym_name;
	}
	public Date getSym_time() {
		return sym_time;
	}
	public void setSym_time(Date sym_time) {
		this.sym_time = sym_time;
	}
	public String getSym_url() {
		return sym_url;
	}
	public void setSym_url(String sym_url) {
		this.sym_url = sym_url;
	}
	
	
}
